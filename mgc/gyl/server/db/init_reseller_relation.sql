  UNIQUE KEY `uk_sku_reseller` (`sku_code`, `reseller_code`),
  KEY `idx_reseller_code` (`reseller_code`),
  KEY `idx_sku_code` (`sku_code`),
  KEY `idx_status` (`status`)
