-- 创建 经销授权关系表
CREATE TABLE IF NOT EXISTS `mst_rltn_sku_reseller` (
  `id`            BIGINT       NOT NULL AUTO_INCREMENT COMMENT '主键',
  `sku_code`      VARCHAR(50)  NOT NULL                COMMENT 'SKU编码',
  `reseller_code` VARCHAR(50)  NOT NULL                COMMENT '经销商编码',
  `reseller_name` VARCHAR(100) DEFAULT NULL            COMMENT '经销商名称',
  `region`        VARCHAR(30)  DEFAULT NULL            COMMENT '所属大区',
  `channel_type`  VARCHAR(20)  DEFAULT NULL            COMMENT '渠道类型: DIST/STORE/ONLINE',
